public class multipleAttributes {
	String fname = "Ruman";
	String lname = "Ali";
	int Age = 17;
	public static void main(String[] args) {
		multipleAttributes myObj = new multipleAttributes();
		System.out.println("My Name is : " +myObj.fname +myObj.lname);
		System.out.println("My Age is : "+myObj.Age);
	}
}